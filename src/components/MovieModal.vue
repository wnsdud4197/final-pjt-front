<template>
  <transition name="modal">
    <div class="modal-mask">
      <div class="modal-wrapper">
        <div class="modal-container">

          <button class="modal-default-button" @click="$emit('close')">
            Close
          </button>

          <div class="modal-body">
            <slot name="body">
              <div class="d-flex">
                <img src="@/assets/keep.png" alt="">
                <img :src="poster_path" class="card-img-top" alt="...">      
                <div>
                  <h3>
                    줄거리
                  </h3>
                  <p>
                    {{ MovieModal.overview }}
                  </p>
                </div>
              </div>
            </slot>
          </div>

          <div class="modal-footer">
            <slot name="footer">
              <div class="d-flex">
                <div>
                  <h3>평점</h3>
                </div>
                <div class="video-container">
                  <iframe  frame :src="videoUrl" frameborder="0"></iframe>
                </div>
              </div>
            </slot>
          </div>
        </div>
      </div>
    </div>
  </transition>
</template>

<script>
export default {
  name: 'MovieModal',
  computed: {
    MovieModal() {
      return this.$store.getters.getModalMovie
    },
    poster_path() {
      return `https://image.tmdb.org/t/p/w500/${this.MovieModal.poster_path}`
    },
    videoUrl() {
      const youtube_key = this.MovieModal.youtube_key
      return `https://www.youtube.com/embed/${youtube_key}`
    }
  }
}
</script>

